    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
         Livros l = new Livros();

        l.setDescricaoLivro(jTextFieldDesc.getText());
        l.setEdicaoLivro(jTextFieldEdic.getText());
        l.setAutorLivro(jTextFieldAutor.getText());
        l.setEditoraLivro(jTextFieldEditora.getText());
        l.setQuantidade(jTextFieldQuantidade.getText());
        l.setIdLivro(id_livro);

        if (salvar) {

            try { 
                LivrosDao.inserir(l);
                LimparCampos();
                System.out.println("Registro realziado com sucesso.");
            } catch (SQLException ex) {
                System.out.println("Não foi possível realizar o registro.");
                ex.printStackTrace();
            }
    }                                            
         else {
            try {
                LivrosDao.editar(l);
                LimparCampos();
                System.out.println("Registro realziado com sucesso.");
            } catch (SQLException ex) {
                System.out.println("Não foi possível realizar o registro.");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
           Livros l = livross.get(jTableLivros.getSelectedRow());
      
       int opc = JOptionPane.showConfirmDialog(null, "Você tem certeza que deseja excluir o" + "livro" +l.getDescricaoLivro()+" - "+ l.getEdicaoLivro());
        
       if (opc == JOptionPane.YES_OPTION){
          try {
              LivrosDao.excluir(l);
              atualizaTabela();
          } catch (SQLException ex) {
              System.out.println("Não foi possível excluir o registro");
          }
       }
                                               
    }//GEN-LAST:event_jButtonExcluirActionPerformed

