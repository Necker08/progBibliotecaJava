     private void atualizaTabela() throws SQLException {
         Livros l = new Livros();
        l.setDescricaoLivro(jTextFieldPesquisa.getText());

        try {
     
            livross = LivrosDao.pesquisar(l);
            DefaultTableModel modeloTabela = (DefaultTableModel) jTableLivros.getModel();
            modeloTabela.setNumRows(0);

            for (Livros livros : livross) {
                modeloTabela.addRow(new Object[]{
                    livros.getDescricaoLivro(),
                    livros.getEdicaoLivro()
                });

            }

        } catch (SQLException ex) {
            System.out.println("Não foi possível carregar os registros.");
        }

    }

    
